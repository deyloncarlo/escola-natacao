    private void jButtonAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAdicionarActionPerformed
        Object item = jComboBoxObjetivos.getSelectedItem();
        boolean objCadastrado = false;
        System.out.println("jtObjetivos.getRowCount() " + jTableObjetivos.getRowCount());
        if (jTableObjetivos.getRowCount() > 0) {
            for (int i = 0; i < jTableObjetivos.getRowCount(); i++) {
                System.out.println("i " + i);
                if (jTableObjetivos.getValueAt(i, 0).equals(item)) {
                    System.out.println("Objetivo ja cadastrado");
                    objCadastrado = true;
                    break;
                }
            }
        }
        if (!objCadastrado) {
            DefaultTableModel model = (DefaultTableModel) jTableObjetivos.getModel();
            String[] a = new String[1];
            if (item.toString().equals("Outros")) {
                a[0] = "Outros: " + jTextFieldOutros.getText();
                jTextFieldOutros.setText("");

            } else {
                a[0] = item.toString();
            }
            model.addRow(a);
            System.out.println("Cadastrou");

        }
        System.out.println("jtObjetivos.getRowCount() " + jTableObjetivos.getRowCount());
    }//GEN-LAST:event_jButtonAdicionarActionPerformed

